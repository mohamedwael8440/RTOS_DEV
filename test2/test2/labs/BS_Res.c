/* Prototypes */
void T_SMS(void* pvData);
void T_Server(void* pvData);

/* OS Objects Decl. */
xSemaphoreHandle		bsRes = NULL;

int main(void)
{
	/* Clock -> Internal 16 MHz */
	RCC_DeInit();				/* Adapt PLL to the internal 16 MHz RC oscillator */
	SystemCoreClockUpdate();	/* Update SystemCoreClock */


	Btn_Init(BTN_RIGHT);
	Leds_Init(0xff);
	Uart_Init(USART2,9600);
	//Buz_Init();
	Uart_SendString(USART2,"RTOS System Started ...\n");

	/* OS Objects Init. and Creation. */
	vSemaphoreCreateBinary(bsRes,1);

	/* Create Tasks */
	xTaskCreate(T_SMS,NULL,100,NULL,2,NULL);
	xTaskCreate(T_Server,NULL,100,NULL,3,NULL);
	/* Start OS or Sched */
	vTaskStartScheduler();

	while(1);
}

void T_SMS(void* pvData){
	while(1){
		if(xSemaphoreTake(bsRes,portMAX_DELAY)){
			Uart_SendString(USART2,"AT+SMS1\r\n");
			Uart_SendString(USART2,"AT+SMS2\r\n");
			xSemaphoreGive(bsRes);
		}
	}
}
void T_Server(void* pvData){
	while(1){
		if(xSemaphoreTake(bsRes,portMAX_DELAY)){
			Uart_SendString(USART2,"AT+Server1\r\n");
			Uart_SendString(USART2,"AT+Server2\r\n");
			xSemaphoreGive(bsRes);
		}
		vTaskDelay(5);
	}
}
